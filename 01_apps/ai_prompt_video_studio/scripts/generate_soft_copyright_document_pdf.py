def p(text: str, style: str = "body") -> Paragraph:
    return Paragraph(text.replace("\n", "<br/>"), STYLES[style])


def bullet(text: str) -> Paragraph:
    return p(f"• {text}", "bullet")


def table(rows: list[list[str]], widths: list[float] | None = None) -> Table:
    data = [[p(cell, "small") for cell in row] for row in rows]
    if widths is None:
        widths = [42 * mm, 118 * mm]
    t = Table(data, colWidths=widths, hAlign="LEFT")
    t.setStyle(
        TableStyle(
            [
                ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#EAF5F1")),
                ("TEXTCOLOR", (0, 0), (-1, -1), colors.HexColor("#24312C")),
                ("GRID", (0, 0), (-1, -1), 0.35, colors.HexColor("#BFD0C8")),
                ("VALIGN", (0, 0), (-1, -1), "TOP"),
                ("LEFTPADDING", (0, 0), (-1, -1), 6),
                ("RIGHTPADDING", (0, 0), (-1, -1), 6),
                ("TOPPADDING", (0, 0), (-1, -1), 5),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 5),
            ]
        )
    )
    return t


def section(story: list, title: str, paras: list[str], bullets: list[str] | None = None, rows: list[list[str]] | None = None) -> None:
    story.append(p(title, "h1"))
    for text in paras:
        story.append(p(text))
    if bullets:
        for item in bullets:
            story.append(bullet(item))
    if rows:
        story.append(Spacer(1, 4))
        story.append(table(rows))
    story.append(Spacer(1, 8))


