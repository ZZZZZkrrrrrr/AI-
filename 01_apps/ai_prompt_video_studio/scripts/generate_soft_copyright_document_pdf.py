def on_page(c: canvas.Canvas, doc: SimpleDocTemplate) -> None:
    page_width, page_height = A4
    c.saveState()
    c.setFont(REGULAR_FONT, 8)
    c.setFillColor(colors.HexColor("#5C6B65"))
    c.drawString(18 * mm, 12 * mm, f"{SOFTWARE_NAME} {VERSION} 用户操作说明书")
    c.drawCentredString(page_width / 2, 12 * mm, f"第 {doc.page} 页")
    c.drawRightString(page_width - 18 * mm, 12 * mm, "文档鉴别材料（一般交存）")
    c.setStrokeColor(colors.HexColor("#C7D4CE"))
    c.line(18 * mm, 17 * mm, page_width - 18 * mm, 17 * mm)
    c.restoreState()


