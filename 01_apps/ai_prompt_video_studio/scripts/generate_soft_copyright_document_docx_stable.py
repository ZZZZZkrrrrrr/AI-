def add_run(paragraph, text: str, size: float = 9.0, bold: bool = False, color: str = "222222") -> None:
    run = paragraph.add_run(text)
    run.font.name = "SimSun"
    run._element.rPr.rFonts.set(qn("w:eastAsia"), "SimSun")
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.color.rgb = RGBColor.from_string(color)


def style_line(paragraph, before: float = 0, after: float = 0, spacing: float = 13.2) -> None:
    paragraph.paragraph_format.space_before = Pt(before)
    paragraph.paragraph_format.space_after = Pt(after)
    paragraph.paragraph_format.line_spacing_rule = WD_LINE_SPACING.EXACTLY
    paragraph.paragraph_format.line_spacing = Pt(spacing)


def set_doc_defaults(doc: Document) -> None:
    section = doc.sections[0]
    section.page_width = Inches(8.27)
    section.page_height = Inches(11.69)
    section.top_margin = Inches(0.55)
    section.bottom_margin = Inches(0.55)
    section.left_margin = Inches(0.72)
    section.right_margin = Inches(0.72)

    normal = doc.styles["Normal"]
    normal.font.name = "SimSun"
    normal._element.rPr.rFonts.set(qn("w:eastAsia"), "SimSun")
    normal.font.size = Pt(9)


def make_page_lines(page_index: int, title: str, facts: list[str]) -> list[str]:
    lines = [
        f"软件名称：{SOFTWARE_NAME}。",
        f"软件简称：{SHORT_NAME}。",
        f"登记版本：{VERSION}。",
        f"本页主题：{title}。",
    ]
    lines.extend(facts)
    filler_index = page_index % len(FILLER_LINES)
    while len(lines) < LINES_PER_PAGE:
        lines.append(FILLER_LINES[filler_index % len(FILLER_LINES)])
        filler_index += 1
    return lines[:LINES_PER_PAGE]


def create_docx() -> None:
    if len(PAGE_TOPICS) != TOTAL_PAGES:
        raise ValueError(f"PAGE_TOPICS must contain {TOTAL_PAGES} topics, got {len(PAGE_TOPICS)}")

    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    doc = Document()
    set_doc_defaults(doc)

    for page_index, (title, facts) in enumerate(PAGE_TOPICS, start=1):
        head = doc.add_paragraph()
        style_line(head, after=4, spacing=15)
        add_run(head, f"{SOFTWARE_NAME} {VERSION} 文档鉴别材料", size=10.5, bold=True, color="0E6F60")

        sub = doc.add_paragraph()
        style_line(sub, after=5, spacing=13.2)
        add_run(sub, f"第{page_index:02d}页 / 共{TOTAL_PAGES}页  ·  {title}", size=9.2, bold=True, color="16211D")

        for line_index, line in enumerate(make_page_lines(page_index, title, facts), start=1):
            para = doc.add_paragraph()
            style_line(para)
            add_run(para, f"{line_index:02d}. {line}", size=9.0, color="222222")

        foot = doc.add_paragraph()
        style_line(foot, before=3, spacing=12)
        add_run(
            foot,
            f"材料类型：文档鉴别材料（一般交存）  生成日期：{date.today().isoformat()}",
            size=8,
            color="66736D",
        )
        if page_index != TOTAL_PAGES:
            foot.add_run().add_break(WD_BREAK.PAGE)

    doc.core_properties.title = f"{SOFTWARE_NAME} {VERSION} 文档鉴别材料"
    doc.core_properties.author = "软件著作权登记材料"
    doc.save(OUTPUT_FILE)


def main() -> None:
    create_docx()
    print(OUTPUT_FILE)
    print(f"pages={TOTAL_PAGES} body_lines_per_page={LINES_PER_PAGE}")


if __name__ == "__main__":
    main()
